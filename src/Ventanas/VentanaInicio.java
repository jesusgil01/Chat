    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String username = txtUsuario.getText();

        if (!txtUsuario.getText().isEmpty()) {
            try {
                cliente = new Cliente(username, "localhost", 9000);
                cliente.init();
                cliente.EnviaMensaje(username);
                String resp = cliente.buffEntrada.readUTF();
                switch (resp) {
                    case "Bienvenido":
                        cliente.RecibirDatos();
                        JOptionPane.showMessageDialog(this, "Bienvenido " + username + "!!!", "Login", 1);
                        VentanaChat vn = new VentanaChat(cliente);
                        vn.setVisible(true);
                        this.dispose();
                        break;
                    case "Ya esta conectado":
                        JOptionPane.showMessageDialog(this, "El usuario ya esta conectado", "Login", 2);
                        break;
                    default:
                        JOptionPane.showMessageDialog(this, "No existe el usuario", "Login", 2);
                        txtUsuario.setText("");
                        break;
                }
            } catch (IOException ex) {
                System.out.println(ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Introduzca un nombre valido");
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

